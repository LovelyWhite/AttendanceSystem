package Pojo;


public class Unread {

  private String uid;
  private String type;
  private long nid; //未读ID


  public String getUid() {
    return uid;
  }

  public void setUid(String uid) {
    this.uid = uid;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public long getNid() {
    return nid;
  }

  public void setNid(long nid) {
    this.nid = nid;
  }

}
