package Pojo;


public class Mac {

  private long mid;
  private long did;
  private String mac;
  private long db;


  public long getMid() {
    return mid;
  }

  public void setMid(long mid) {
    this.mid = mid;
  }


  public long getDid() {
    return did;
  }

  public void setDid(long did) {
    this.did = did;
  }


  public String getMac() {
    return mac;
  }

  public void setMac(String mac) {
    this.mac = mac;
  }


  public long getDb() {
    return db;
  }

  public void setDb(long db) {
    this.db = db;
  }

}
