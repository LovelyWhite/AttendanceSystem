package Pojo;


public class StuSign {

  private long sid;
  private long cid;
  private long nid;
  private long uid;


  public long getSid() {
    return sid;
  }

  public void setSid(long sid) {
    this.sid = sid;
  }


  public long getCid() {
    return cid;
  }

  public void setCid(long cid) {
    this.cid = cid;
  }

  public long getNid() {
    return nid;
  }

  public void setNid(long nid) {
    this.nid = nid;
  }

  public long getUid() {
    return uid;
  }

  public void setUid(long uid) {
    this.uid = uid;
  }

}
