package Model;


public class SchoolCourse {

  private long courseid;
  private String tusername;
  private String name;
  private String currentweek;
  private String property;
  private double credit;
  private String classweek;
  private String workweek;
  private String time;
  private String description;
  private String dest;
  private String isodd;


  public long getCourseid() {
    return courseid;
  }

  public void setCourseid(long courseid) {
    this.courseid = courseid;
  }


  public String getTusername() {
    return tusername;
  }

  public void setTusername(String tusername) {
    this.tusername = tusername;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getCurrentweek() {
    return currentweek;
  }

  public void setCurrentweek(String currentweek) {
    this.currentweek = currentweek;
  }


  public String getProperty() {
    return property;
  }

  public void setProperty(String property) {
    this.property = property;
  }


  public double getCredit() {
    return credit;
  }

  public void setCredit(double credit) {
    this.credit = credit;
  }


  public String getClassweek() {
    return classweek;
  }

  public void setClassweek(String classweek) {
    this.classweek = classweek;
  }


  public String getWorkweek() {
    return workweek;
  }

  public void setWorkweek(String workweek) {
    this.workweek = workweek;
  }


  public String getTime() {
    return time;
  }

  public void setTime(String time) {
    this.time = time;
  }


  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }


  public String getDest() {
    return dest;
  }

  public void setDest(String dest) {
    this.dest = dest;
  }


  public String getIsodd() {
    return isodd;
  }

  public void setIsodd(String isodd) {
    this.isodd = isodd;
  }

}
